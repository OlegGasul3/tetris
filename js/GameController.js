class GameController {
    constructor() {
        document.addEventListener('keydown', function(event) {
            switch (event.keyCode) {
                case 38:
                    // Rotate
                    break;

                case 37:
                    // Left
                    break;

                case 39:
                    // Right
                    break;

                case 40:
                    // Down
                    break;

                case 32:
                    // Space
                    break;
            }
        }, false);
    }
}